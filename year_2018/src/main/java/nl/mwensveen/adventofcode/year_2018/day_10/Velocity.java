package nl.mwensveen.adventofcode.year_2018.day_10;

public class Velocity {
    private final Integer velocityX;
    private final Integer velocityY;

    public Velocity(Integer velocityX, Integer velocityY) {
        this.velocityX = velocityX;
        this.velocityY = velocityY;
    }

    public Integer getVelocityX() {
        return velocityX;
    }

    public Integer getVelocityY() {
        return velocityY;
    }
}
